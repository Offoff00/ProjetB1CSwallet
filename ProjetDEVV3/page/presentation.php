<h1>🎓 Présentation du projet</h1>
<br>
<h2>Pourquoi ce projet ?</h2>
<p>
Dans le cadre de notre formation Bachelor 1 CyberSécurité à l’EFREI, nous avons voulu créer un outil pédagogique et ludique pour découvrir l'univers des cryptomonnaies de façon sécurisée. <br>
Notre objectif : apprendre à gérer un portefeuille crypto sans aucun risque financier, dans une interface simple et réaliste.
</p>
<br>
<h2>Nos objectifs</h2>
<ul>
  <li>🚀 Sensibiliser aux bases de la finance décentralisée (DeFi)</li>
  <li>🧠 Apprendre à acheter, vendre et suivre un solde virtuel</li>
  <li>👨‍💻 Développer des réflexes de sécurité numérique</li>
  <li>🎯 Créer un projet réel en collaboration avec le BDE</li>
</ul>
<br>
<h2>Comment ça fonctionne ?</h2>
<p>
Chaque étudiant possède un portefeuille fictif qui lui permet de :
</p>
<ul>
  <li>Se connecter via son identifiant MyEFREI</li>
  <li>Gérer ses cryptos virtuelles (achat/vente/simulation)</li>
  <li>Suivre ses performances grâce à un système de points et de graphiques</li>
</ul>
<br>
<h2>Pour conclure </h2>
<p>Ce projet, 100% fictif, est un exercice pédagogique. Il permet aux étudiants d'apprendre en s'amusant, tout en mettant en pratique leurs compétences techniques.</p>
